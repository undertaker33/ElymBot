package com.astrbot.android.data.db

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.astrbot.android.model.ConfigProfile

@Entity(tableName = "config_profiles")
data class ConfigProfileEntity(
    @PrimaryKey val id: String,
    val name: String,
    val defaultChatProviderId: String,
    val defaultVisionProviderId: String,
    val defaultSttProviderId: String,
    val defaultTtsProviderId: String,
    val sttEnabled: Boolean,
    val ttsEnabled: Boolean,
    val alwaysTtsEnabled: Boolean,
    val ttsReadBracketedContent: Boolean,
    val textStreamingEnabled: Boolean,
    val voiceStreamingEnabled: Boolean,
    val streamingMessageIntervalMs: Int,
    val realWorldTimeAwarenessEnabled: Boolean,
    val imageCaptionTextEnabled: Boolean,
    val webSearchEnabled: Boolean,
    val proactiveEnabled: Boolean,
    val ttsVoiceId: String,
    val imageCaptionPrompt: String,
    val adminUidsJson: String,
    val sessionIsolationEnabled: Boolean,
    val wakeWordsJson: String,
    val wakeWordsAdminOnlyEnabled: Boolean,
    val privateChatRequiresWakeWord: Boolean,
    val replyTextPrefix: String,
    val quoteSenderMessageEnabled: Boolean,
    val mentionSenderEnabled: Boolean,
    val replyOnAtOnlyEnabled: Boolean,
    val whitelistEnabled: Boolean,
    val whitelistEntriesJson: String,
    val logOnWhitelistMiss: Boolean,
    val adminGroupBypassWhitelistEnabled: Boolean,
    val adminPrivateBypassWhitelistEnabled: Boolean,
    val ignoreSelfMessageEnabled: Boolean,
    val ignoreAtAllEventEnabled: Boolean,
    val replyWhenPermissionDenied: Boolean,
    val rateLimitWindowSeconds: Int,
    val rateLimitMaxCount: Int,
    val rateLimitStrategy: String,
    val keywordDetectionEnabled: Boolean,
    val keywordPatternsJson: String,
    val sortIndex: Int,
    val updatedAt: Long,
)

fun ConfigProfileEntity.toProfile(): ConfigProfile {
    return ConfigProfile(
        id = id,
        name = name,
        defaultChatProviderId = defaultChatProviderId,
        defaultVisionProviderId = defaultVisionProviderId,
        defaultSttProviderId = defaultSttProviderId,
        defaultTtsProviderId = defaultTtsProviderId,
        sttEnabled = sttEnabled,
        ttsEnabled = ttsEnabled,
        alwaysTtsEnabled = alwaysTtsEnabled,
        ttsReadBracketedContent = ttsReadBracketedContent,
        textStreamingEnabled = textStreamingEnabled,
        voiceStreamingEnabled = voiceStreamingEnabled,
        streamingMessageIntervalMs = streamingMessageIntervalMs,
        realWorldTimeAwarenessEnabled = realWorldTimeAwarenessEnabled,
        imageCaptionTextEnabled = imageCaptionTextEnabled,
        webSearchEnabled = webSearchEnabled,
        proactiveEnabled = proactiveEnabled,
        ttsVoiceId = ttsVoiceId,
        imageCaptionPrompt = imageCaptionPrompt,
        adminUids = adminUidsJson.parseJsonStringList(),
        sessionIsolationEnabled = sessionIsolationEnabled,
        wakeWords = wakeWordsJson.parseJsonStringList(),
        wakeWordsAdminOnlyEnabled = wakeWordsAdminOnlyEnabled,
        privateChatRequiresWakeWord = privateChatRequiresWakeWord,
        replyTextPrefix = replyTextPrefix,
        quoteSenderMessageEnabled = quoteSenderMessageEnabled,
        mentionSenderEnabled = mentionSenderEnabled,
        replyOnAtOnlyEnabled = replyOnAtOnlyEnabled,
        whitelistEnabled = whitelistEnabled,
        whitelistEntries = whitelistEntriesJson.parseJsonStringList(),
        logOnWhitelistMiss = logOnWhitelistMiss,
        adminGroupBypassWhitelistEnabled = adminGroupBypassWhitelistEnabled,
        adminPrivateBypassWhitelistEnabled = adminPrivateBypassWhitelistEnabled,
        ignoreSelfMessageEnabled = ignoreSelfMessageEnabled,
        ignoreAtAllEventEnabled = ignoreAtAllEventEnabled,
        replyWhenPermissionDenied = replyWhenPermissionDenied,
        rateLimitWindowSeconds = rateLimitWindowSeconds,
        rateLimitMaxCount = rateLimitMaxCount,
        rateLimitStrategy = rateLimitStrategy,
        keywordDetectionEnabled = keywordDetectionEnabled,
        keywordPatterns = keywordPatternsJson.parseJsonStringList(),
    )
}

